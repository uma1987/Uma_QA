
public class Switch {
	public static void main(String args[])
	{
		int n =3;
		
		switch(n)
		{
		case 1:
			System.out.println("Sunday");
			break;
		case 2:
		System.out.println("monday");
		break;
		case 3:
			System.out.println("Tuesday");
			break;
		case 4:
			System.out.println("wednesday");
			break;
		case 5:
			System.out.println("Thursday");
			break;
			default:
				System.out.println("Invalid");
		}
	}

}
